def minimumAbsoluteDifference(arr):
    arr.sort()
    
    min_number = abs(arr[0]-arr[1])
    for i in range(2, len(arr)):
        if abs(arr[i-1]-arr[i]) < min_number:
            min_number = abs(arr[i-1]-arr[i])
    return min_number
        
        

